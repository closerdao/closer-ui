export function countryCodeToLocale(
  countryCode: string | undefined | null,
): string {
  if (!countryCode || typeof countryCode !== 'string') {
    return 'en-US';
  }
  const key = countryCode.trim().toUpperCase();
  return COUNTRY_CODE_TO_LOCALE[key] ?? 'en-US';
}
